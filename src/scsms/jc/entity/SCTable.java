package scsms.jc.entity;

public class SCTable {
     private String stu_name;
     private String tec_name;
     private float grade;
     private String cous_name;
	public String getStu_name() {
		return stu_name;
	}
	public void setStu_name(String stu_name) {
		this.stu_name = stu_name;
	}
	public String getTec_name() {
		return tec_name;
	}
	public void setTec_name(String tec_name) {
		this.tec_name = tec_name;
	}
	public float getGrade() {
		return grade;
	}
	public void setGrade(float grade) {
		this.grade = grade;
	}
	public String getCous_name() {
		return cous_name;
	}
	public void setCous_name(String cous_name) {
		this.cous_name = cous_name;
	}
  	
}
